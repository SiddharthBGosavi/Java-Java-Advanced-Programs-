public class Primitive_data_types {
    /**
     * Demonstrates declaration and initialization of Java primitive data types.
     * Prints "Hello world" to the console.
     */
    public static void main(String[] args) {
        // Integral types
        int num1 = 8;
        byte by = 127;      // Max value for byte
        short sh = 558;
        long l = 5_884L;    // Use uppercase 'L' for clarity

        // Floating-point types
        float f = 5.8f;
        double d = 5.8;

        // Character type
        char c = 'k';

        // Boolean type
        boolean b = true;

        System.out.println("Hello world");
    }
}